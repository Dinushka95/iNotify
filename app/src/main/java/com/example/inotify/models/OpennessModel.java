package com.example.inotify.models;

public class OpennessModel {

    private String opennessId;
    private String date;
    private String newlyapps;
    private String allappusage;
    private String socialappusage;
    private String noofsocialapps;
    private String noofcommunicationapps;
    private String communicationappusage;

    public String getOpennessId() {
        return opennessId;
    }

    public void setOpennessId(String opennessId) {
        this.opennessId = opennessId;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getNewlyapps() {
        return newlyapps;
    }

    public void setNewlyapps(String newlyapps) {
        this.newlyapps = newlyapps;
    }

    public String getAllappusage() {
        return allappusage;
    }

    public void setAllappusage(String allappusage) {
        this.allappusage = allappusage;
    }

    public String getSocialappusage() {
        return socialappusage;
    }

    public void setSocialappusage(String socialappusage) {
        this.socialappusage = socialappusage;
    }

    public String getNoofsocialapps() {
        return noofsocialapps;
    }

    public void setNoofsocialapps(String noofsocialapps) {
        this.noofsocialapps = noofsocialapps;
    }

    public String getNoofcommunicationapps() {
        return noofcommunicationapps;
    }

    public void setNoofcommunicationapps(String noofcommunicationapps) {
        this.noofcommunicationapps = noofcommunicationapps;
    }

    public String getCommunicationappusage() {
        return communicationappusage;
    }

    public void setCommunicationappusage(String communicationappusage) {
        this.communicationappusage = communicationappusage;
    }


}
